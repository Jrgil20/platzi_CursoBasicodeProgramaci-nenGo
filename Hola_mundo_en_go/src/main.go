// Código de ejemplo para imprimir "Hola Mundo" en la consola en Go.

// Declara el paquete main.
// Los programas en Go se ejecutan en el paquete main.
// Si se declara un paquete diferente a main, el programa no se ejecutará.
package main

// Importa el paquete fmt que contiene funciones para imprimir en la consola.
// fmt es la abreviatura de "format".
import "fmt"

// main es la función principal que se ejecuta al iniciar el programa.
// Imprime "Hola Mundo" en la consola.
func main() {
	fmt.Println("Hola Mundo")
	// fmt.Println imprime en la consola y agrega un salto de línea al final.
}
